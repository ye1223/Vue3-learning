<template>
    <div>
        <button @click="isShow=true">点我弹窗</button>
        <!-- teleport将一段html结构传送到指定位置（html内容指定中任意位置） -->
        <!-- 将下面片段传送到body结构下 ，（可以写css选择器）-->
        <teleport to='body'>
            <div v-if="isShow" class="mask">
                <div class="dialog">
                <h3>我是一个弹窗</h3>
                <h4>some contents</h4>
                <h4>some contents</h4>
                <h4>some contents</h4>
                <button @click="isShow=false">关闭弹窗</button>
                </div>
            </div>
        </teleport>
    </div>
</template>

<script>
import {ref} from 'vue'
export default {
    name:'Dialog',
    setup(){
        let isShow = ref(false)
        return{
            isShow
        }
    }
}
</script>

<style>
.mask{
    position: absolute;
    top: 0;
    bottom: 0;
    left: 0;
    right: 0;
    background-color: rgba(0,0,0,0.5);
}
.dialog{
    width: 300px;
    height: 400px;
    background-color: green;

    text-align: center;
    /* 此处相对body定位 */
    position: absolute;
    top: 50%;
    left: 50%;
    /* 相对自身的50% */
    transform: translate(-50%,-50%);
}
</style>